package com.cn.mybatis.pojo;

public class OrderDetails {
	private int id;
	private int	iterms_id;
	private int	items_number;
	private int	orders_id;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIterms_id() {
		return iterms_id;
	}
	public void setIterms_id(int iterms_id) {
		this.iterms_id = iterms_id;
	}
	public int getItems_number() {
		return items_number;
	}
	public void setItems_number(int items_number) {
		this.items_number = items_number;
	}
	public int getOrders_id() {
		return orders_id;
	}
	public void setOrders_id(int orders_id) {
		this.orders_id = orders_id;
	}
	

}
